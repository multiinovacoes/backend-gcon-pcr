package br.com.multiinovacoes.gcon.model.request;

import io.swagger.annotations.ApiModelProperty;


public class FormaRespostaRequest {
	
	
	@ApiModelProperty(value = "id da forma de resposta")
	private Long id;

	@ApiModelProperty(value = "Descrição da forma de resposta")
	private String descricao;
	
	@ApiModelProperty(value = "Status da forma de resposta")
	private Integer status;
	
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		FormaRespostaRequest other = (FormaRespostaRequest) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}
	
	
	
	

}
