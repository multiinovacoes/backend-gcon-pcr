package br.com.multiinovacoes.gcon.graphics;

import java.util.List;

public class StackedColumn {

	private List<Label> label;
	
	private List<SeriesName> seriesName;
	
	public StackedColumn() {
	}

	public List<Label> getLabel() {
		return label;
	}

	public void setLabel(List<Label> label) {
		this.label = label;
	}

	public List<SeriesName> getSeriesName() {
		return seriesName;
	}

	public void setSeriesName(List<SeriesName> seriesName) {
		this.seriesName = seriesName;
	}

	
	

}
